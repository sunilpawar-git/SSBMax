**For Army**:
"Sir, Army appeals to me because of its primary role in ground operations and direct leadership of soldiers. The diversity of postings—from high-altitude Siachen to desert borders—offers varied experiences. Leading troops in challenging terrains and being part of India's largest defense force attracts me. The combined arms operations requiring coordination of infantry, armor, artillery fascinates me."

