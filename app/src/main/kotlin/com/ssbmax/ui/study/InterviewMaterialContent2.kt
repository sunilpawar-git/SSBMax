# Common Interview Questions

While every SSB interview is unique, certain categories of questions appear consistently. Preparing thoughtful, honest responses to these common questions builds confidence and helps you articulate your story effectively during the actual interview.

## Personal Background Questions

### Q1: "Tell me about yourself"

**What IO Wants**: Concise personal introduction, not life history

**Structure** (2-3 minutes):
- Name, age, hometown
- Family background (brief)
- Educational journey (highlights)
- Key interests/hobbies
- Why armed forces

**Sample Response**:
"Good morning, Sir. I'm Rahul Sharma, 22 years old, from Jaipur, Rajasthan. I come from a middle-class family—my father is a school teacher and my mother is a homemaker. I have one younger sister who is pursuing her graduation. I completed my B.Tech in Mechanical Engineering from XYZ College with 75% marks. Since childhood, I've been active in sports, particularly basketball, and was captain of my college team. My interest in joining the armed forces developed during my NCC training and interaction with serving officers. I've been preparing for SSB for the past 8 months and am committed to serving the nation as an officer."

**Common Follow-ups**:
- Tell me more about your family
- Why did you choose engineering?
- What did you learn from NCC?

